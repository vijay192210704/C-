#include <iostream>
using namespace std;

int main() {
    float a, b;
    cout << "Enter two floating-point numbers: ";
    cin >> a >> b;

    cout << "Addition: " << a + b << endl;
    cout << "Subtraction: " << a - b << endl;
    cout << "Multiplication: " << a * b << endl;
    cout << "Division: " << a / b << endl;

    return 0;
}

